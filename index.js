function somarDoisNumeros(numero1, numero2){
    let soma = numero1 + numero2;
    return('A soma dos dois numeros é: ' + soma)
}

function subtracaoEntreDoisNumeros(numero1, numero2){
    let subtracao = numero1 - numero2;
    return('A subtracao do primeiro pelo segundo número é: ' + subtracao)
}

function multiplicacaoDeDoisNumeros(numero1, numero2){
    let multiplicacao = numero1 * numero2;
    return('A multiplicação dos dois números é: ' + multiplicacao)
}


function divisaoEntreDoisNumeros(numero1, numero2){
    let divisao = numero1 / numero2;
    return('A divisão do primeiro pelo segundo número é: ' + divisao)
}


console.log(somarDoisNumeros(12 , 4))
console.log(subtracaoEntreDoisNumeros(12 , 4))
console.log(multiplicacaoDeDoisNumeros(12 , 4))
console.log(divisaoEntreDoisNumeros(12 , 4))

